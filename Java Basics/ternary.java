public class ternary {
    public static void main(String[] args) {
        int a = 10;
        int b = 23;
        int max = 0;
        max = a>b ? a:b;
        //condition ? expression1 : expression2;
        //if condition true expression 1 will execute else expression 2
        System.out.println("max no. is :"+max);
    }
}
